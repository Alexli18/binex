"""CLI `binex init` command — interactive project initialisation."""

from __future__ import annotations

from pathlib import Path

import click

from binex.cli.providers import PROVIDERS, ProviderConfig

# Ordered list for the numbered menu.
_PROVIDER_LIST: list[ProviderConfig] = list(PROVIDERS.values())


# ---- templates ---------------------------------------------------------------

def _workflow_yaml(project_name: str, agent_prefix: str, model: str) -> str:
    agent_ref = f"{agent_prefix}{model}"
    return f'''name: "{project_name}"
description: "Generated by binex init"
nodes:
  planner:
    agent: "{agent_ref}"
    system_prompt: "Plan the research on the given topic"
    inputs:
      topic: "${{user.topic}}"
    outputs: [plan]
    depends_on: []
  researcher:
    agent: "{agent_ref}"
    system_prompt: "Research based on the plan"
    inputs:
      plan: "${{planner.plan}}"
    outputs: [findings]
    depends_on: [planner]
  writer:
    agent: "{agent_ref}"
    system_prompt: "Write a summary based on research findings"
    inputs:
      findings: "${{researcher.findings}}"
    outputs: [summary]
    depends_on: [researcher]
'''


def _env_example(env_var: str | None) -> str:
    lines = ["# Environment variables for Binex project", ""]
    if env_var:
        lines.append(f"{env_var}=your-key-here")
        lines.append("")
    return "\n".join(lines)


